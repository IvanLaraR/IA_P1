#para sumar variables 
palabra_1 = 'Programación' #declaro una variable que va a ser el primer mensaje
palabra_2 = 'facil' #declaro la segunda variable que va a ser el segundo mensaje
nombre_completo = palabra_1 + palabra_2 #declaro una tercera variable que va tener las dos primeras variavles y las va sumar 
print(nombre_completo)#se usa el print para imprimir el mensaje y mostarlo en la pantalla

#para sumar varibles con espacio entre si
palabra_1 = 'Programación' #declaro una variable que va a ser el primer mensaje
palabra_2 = 'facil' #declaro la segunda variable que va a ser el segundo mensaje
nombre_completo = palabra_1 + '' + palabra_2 #declaro una tercera variable que va a tener las dos primeras variavles con un espacion por las '' y las va sumar 
print(nombre_completo)#se usa el print para imprimir el mensaje y mostarlo en la pantalla

