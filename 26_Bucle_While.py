num = 5 #se declara una variable y se iguala a un entero

while num < 10: #se crea un bucle infinito hasta que por un medio externo o manual detenga la ejecucion 
	print(num)#se imprime la variable
	num += 1 #se añade un incremento para que cambie su valor numerico

#se usa el += para imcremento y -= para el decramiento
