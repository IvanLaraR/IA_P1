
#ejemplo de un bucle infinito

x = 1 #se declara una variable y se iguala a un entero

while x < 10: #se crea un bucle infinito hasta que por un medio externo o manual detenga la ejecucion 
	print(x)#se imprime la variable
	x += 1 #se añade un incremento para que cambie su valor numerico

#se usa el += para imcremento y -= para el decramiento
