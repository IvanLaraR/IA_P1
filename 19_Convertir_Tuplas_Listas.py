
#Convertir una lista a una tupla
lista = ["Honda", "Fiat", "BMW", "Ford", "Kia", "Bentley", "Tesla", "Toyota"]#se crea una lista con 8 elementos diferentes
tupla = tuple(lista)#se crea una variable tupla donde se usa el metodo tuple que conviertir la lista en una tupla
print(tupla)#se imprime la nueva tupla
print(type(tupla)) #se utiliza el metodo type para saber que clase es la variable

