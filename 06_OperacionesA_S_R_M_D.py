#Como hacer operaciones Matematicas
suma = 10 + 5 + 5 #Se declara una variable y se le asigna un valor numerico que se suma
resta = 10 - 5 - 5 #se declara una variable y se le asigna un valor numerico que se resta
multiplicacion = 10 * 5 * 5 #se declara una variable y se le asigna un valor numerico que se multiplica
division = 10 / 5 / 5 #se declara una variable y se le asigna un valor numerico que se divide 
print("Suma: " , suma ,  "\nResta: " , resta , "\nMultiplicación: " , multiplicacion , "\nDivisión: " , division)# se usa el print para imprimir los valores y se llama a la variable para que aparezca

print("\n")#se hace un salto de linea para que se vea mas ordenado

#se pueden combinar diferentes operadores
operacion = 10 - 5 * 6 / 2 + 8 #se declara una variable y se le asigna el valor de una operacion matematica
print("Solucion: " , operacion) #se usa el print para imprimirlo y se llama a la variable

print("\n")#se hace un salto de linea para que se vea mas ordenado 
#se usa () para darle prioridad a ciertas operaciones 
