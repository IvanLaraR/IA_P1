#Como hacer operaciones Matematicas

suma = 500 + 2 +20 +50 #Se declara una variable y se le asigna un valor numerico que se suma
resta = 8 - 4 - 5 #se declara una variable y se le asigna un valor numerico que se resta
multiplicacion = 5 * 5 * 5 #se declara una variable y se le asigna un valor numerico que se multiplica
division = 100 / 2 / 50 #se declara una variable y se le asigna un valor numerico que se divide 
print("" , suma ,  "\n: " , resta , "\n: " , multiplicacion , "\n: " , division)# se usa el print para imprimir los valores y se llama a la variable para que aparezca

print("\n")#se hace un salto de linea para que se vea mas ordenado

#se pueden combinar diferentes operadores
var = 10 + 5 * 6 + 2 - 8 / 2 #se declara una variable y se le asigna el valor de una operacion matematica
print("Solucion: " , var) #se usa el print para imprimirlo y se llama a la variable

print("\n")#se hace un salto de linea para que se vea mas ordenado 
#se usa () para darle prioridad a ciertas operaciones 
