#El método title(poner la primera palabra en mayusculas)

var = "curso python" #se declara una variable que va a ser el primer mensaje en un string
var = var.title() #se le cambia el valor a la varibale poniendo en segida el metodo .title()
print(var)#se usa el print para imprimir y mostrarlo en la pantalla

#////Abreviando el codigo////

var = "curso python".title()#se declara la variable con su mensaje string y se le agrega el metodo .title()
print(var)#se usa el print para imprimir y mostarlo en la pantalla

#El método upper(todos los caracteres de un string se hacen mayusculas)
var = "curso python".upper()#se declara la variable con su mensaje string y se le agrega el metodo .upper()
print(var)#se usa el print para imprimir y mostrarlo en la pantalla

#El metodo lower (todos los caracteres de un string se hacen minusculas)
var = "CURSO pYtHoN".lower()#se declara una variable con su mensaje string y se le agrega el metodo .lower()
print(var)#se usa el print para imprimir y mostrarlo en la pantalla
